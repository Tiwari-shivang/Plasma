package com.example.plasma.Structure;

public class Request {
    public String Age, Gender, Weight, BMI, BMR, BloodGroup, Address, Mobile;
    public Request (String Age, String Gender, String Weight, String BMI, String BMR, String BloodGroup, String Address, String Mobile){
        this.Age = Age;
        this.Gender = Gender;
        this.Weight = Weight;
        this.BMI = BMI;
        this.BMR = BMR;
        this.BloodGroup = BloodGroup;
        this.Address = Address;
        this.Mobile = Mobile;
    }
}
